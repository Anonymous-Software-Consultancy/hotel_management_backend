CREATE TABLE amenities (
    id INT AUTO_INCREMENT PRIMARY KEY,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    air_conditioning BOOLEAN DEFAULT FALSE,
    balcony BOOLEAN DEFAULT FALSE,
    bathtub BOOLEAN DEFAULT FALSE,
    ceiling_fan BOOLEAN DEFAULT FALSE,
    clothes_dryer BOOLEAN DEFAULT FALSE,
    connecting_rooms BOOLEAN DEFAULT FALSE,
    cooker BOOLEAN DEFAULT FALSE,
    dining_area BOOLEAN DEFAULT FALSE,
    electric_kettle BOOLEAN DEFAULT FALSE,
    garden_view BOOLEAN DEFAULT FALSE,
    hairdryer BOOLEAN DEFAULT FALSE,
    hot_water BOOLEAN DEFAULT FALSE,
    ironing_set BOOLEAN DEFAULT FALSE,
    kitchenete BOOLEAN DEFAULT FALSE,
    microwave_oven BOOLEAN DEFAULT FALSE,
    minibar BOOLEAN DEFAULT FALSE,
    mountain_or_hill_view BOOLEAN DEFAULT FALSE,
    non_smoking_room BOOLEAN DEFAULT FALSE,
    pool_view BOOLEAN DEFAULT FALSE,
    power_outlet BOOLEAN DEFAULT FALSE,
    private_beach BOOLEAN DEFAULT FALSE,
    safe_or_locker BOOLEAN DEFAULT FALSE,
    smoking_room BOOLEAN DEFAULT FALSE,
    tea_and_offee BOOLEAN DEFAULT FALSE,
    telephone BOOLEAN DEFAULT FALSE,
    toiletries BOOLEAN DEFAULT FALSE,
    tv BOOLEAN DEFAULT FALSE,
    wifi BOOLEAN DEFAULT FALSE,
    room_id INT,
    FOREIGN KEY (room_id) REFERENCES rooms(id) ON DELETE CASCADE
);
